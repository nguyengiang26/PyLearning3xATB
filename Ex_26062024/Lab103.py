# OOPs - Object Oriented Programming

# Procedural Programming vs OOPs
# Procedural Programming: A programming paradigm that uses procedures or routines to perform tasks. 
#                         It focuses on the sequence of actions to be performed.\
#                         Ex: C, Pascal
# OOPs: A programming paradigm that uses objects to represent data and methods to manipulate that data. 
#       It focuses on the organization of code into reusable components.
#       Ex: Python, Java, C++

# OOPs Concepts:
# Class & Object:
#   1. Class: A BLUEPRINT for creating objects. It defines the properties and behaviors that the objects will have.
#   2. Object/Instance: An instance of a class. It represents a specific realization of the class.
# Atributes & Methods:
#   1. Attributes/Properties: Characteristics of a class or object. They define the state of the object.
#      Example: name, age, color, etc.
#   2. Methods/Behaviour/Funationality: Functions defined within a class that operate on the attributes of the class. 
#                                       They define the behavior of the object.
#      Example: eat(), sleep(), run(), etc.
# Constructor & Self:
#   1. Constructor: A special method that is called when an object is created. It initializes the object's properties.
#      Example: __init__() method in Python.
#   2. Self: A reference to the current instance of the class. It is used to access the object's properties and methods.
#      Example: self.name, self.age, etc.
# Inheritance:
#   A mechanism that allows a class to inherit properties and methods from another class. It promotes code reusability.
#   Example: class Dog(Animal): where Dog is a subclass of Animal. The Dog class inherits properties and methods from the Animal class.
