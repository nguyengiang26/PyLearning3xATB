# Literals
# var_name = variable_value
name = "John Doe"
# var_name -> Identifiers
# variable_value -> Literals
# Literals are actual values assigned to variables (data types of variables)
# Literals can be Numeric and Non-Numeric

# Numeric Literals
x = 10  # Integer literal
y = 3.14  # Floating-point literal
z = 2 + 3j  # Complex literal

# Non-Numeric Literals
a = 'Hello'  # String literal
b = b"Hello"  # Bytes literal
c = """Hello"""  # Multiline string literal
d = '''Hello'''  # Multiline string literal

e = True  # Boolean literal
f = False  # Boolean literal
g = None  # None literal

my_list = [1, 2, 3]  # List literal
my_tuple = (1, 2, 3)  # Tuple literal
my_set = {1, 2, 3}  # Set literal
my_dict = {'a': 1, 'b': 2, 'c': 3}  # Dictionary literal
